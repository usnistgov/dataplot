        CALL DPCLFI(ICAPNU,ICAPNA,ICAPST,ICAPFO,ICAPAC,ICAPPR,ICAPCS,   &
                    IENDFI,IREWIN,ISUBN0,IERRFI,IBUGS2,ISUBRO,IERROR)
!
!       STEP 2: RE-OPEN CAPTURE FILE IN APPEND MODE
!
        OPEN(ICAPNU,FILE=ICAPNA,STATUS="OLD",POSITION="APPEND",   &
             ACTION="WRITE")
      ENDIF
!
!     2015/12: IF CAPTURE SPLIT IS ON AND CAPTURE SWITCH IS
!              ON, CLOSE THE CAPTURE FILE AND THEN RE-OPEN IN
!              APPEND MODE.
!
      IF(IFOUND.EQ.'YES' .AND. ICAPSW.EQ.'ON' .AND.   &
         ICAPSP.EQ.'ON')THEN
!
!       STEP 1: CLOSE CAPTURE FILE (ONLY IF COUNTER > 0)
!
        IF(ICAPCN.GT.0)THEN
          IENDFI='ON'
          IREWIN='OFF'
          CALL DPCLFI(ICPNU2,ICAPN2,ICAPS2,ICAPF2,ICAPA2,ICAPP2,ICAPC2,   &
                      IENDFI,IREWIN,ISUBN0,IERRFI,IBUGS2,ISUBRO,IERROR)
        ENDIF
!
!
!       STEP 2: INCREMENT THE COUNTER AND OPEN A NEW CAPTURE
!               FILE WITH COUNTER IN NAME.
!
        ICAPCN=ICAPCN+1
        ICAPN2=' '
        ICAPN2(1:80)=ICAPNA(1:80)
        ILAST=80
        IPEROD=-1
        DO 27001 II=80,1,-1
          IF(ICAPN2(II:II).NE.' ')THEN
            ILAST=II
            GO TO 27003
          ENDIF
27001   CONTINUE
27003   CONTINUE
!
        DO 27006 II=80,1,-1
          IF(ICAPN2(II:II).EQ.'.')THEN
            IPEROD=II
            GO TO 27008
          ENDIF
27006   CONTINUE
27008   CONTINUE
!
        IF(ICAPCN.GE.10000 .AND. ICAPCN.LE.99999)THEN
          IF(IPEROD.LE.0)THEN
            ILAST=ILAST+1
            ICAPN2(ILAST:ILAST)='.'
            WRITE(ICAPN2(ILAST+1:ILAST+5),'(I5)')ICAPCN
          ELSE
            ICAPN2(IPEROD+6:ILAST+6)=ICAPN2(IPEROD:ILAST)
            ILAST=IPEROD
            ICAPN2(ILAST:ILAST)='_'
            WRITE(ICAPN2(ILAST+1:ILAST+5),'(I5)')ICAPCN
          ENDIF
        ELSEIF(ICAPCN.GE.1000 .AND. ICAPCN.LE.9999)THEN
          IF(IPEROD.LE.0)THEN
            ILAST=ILAST+1
            ICAPN2(ILAST:ILAST)='.'
            WRITE(ICAPN2(ILAST+1:ILAST+4),'(I4)')ICAPCN
          ELSE
            ICAPN2(IPEROD+5:ILAST+5)=ICAPN2(IPEROD:ILAST)
            ILAST=IPEROD
            ICAPN2(ILAST:ILAST)='_'
            WRITE(ICAPN2(ILAST+1:ILAST+4),'(I4)')ICAPCN
          ENDIF
        ELSEIF(ICAPCN.GE.100 .AND. ICAPCN.LE.999)THEN
          IF(IPEROD.LE.0)THEN
            ILAST=ILAST+1
            ICAPN2(ILAST:ILAST)='.'
            WRITE(ICAPN2(ILAST+1:ILAST+3),'(I3)')ICAPCN
          ELSE
            ICAPN2(IPEROD+4:ILAST+4)=ICAPN2(IPEROD:ILAST)
            ILAST=IPEROD
            ICAPN2(ILAST:ILAST)='_'
            WRITE(ICAPN2(ILAST+1:ILAST+3),'(I3)')ICAPCN
          ENDIF
        ELSEIF(ICAPCN.GE.10 .AND. ICAPCN.LE.99)THEN
          IF(IPEROD.LE.0)THEN
            ILAST=ILAST+1
            ICAPN2(ILAST:ILAST)='.'
            WRITE(ICAPN2(ILAST+1:ILAST+2),'(I2)')ICAPCN
          ELSE
            ICAPN2(IPEROD+3:ILAST+3)=ICAPN2(IPEROD:ILAST)
            ILAST=IPEROD
            ICAPN2(ILAST:ILAST)='_'
            WRITE(ICAPN2(ILAST+1:ILAST+2),'(I2)')ICAPCN
          ENDIF
        ELSEIF(ICAPCN.GE.1 .AND. ICAPCN.LE.9)THEN
          IF(IPEROD.LE.0)THEN
            ILAST=ILAST+1
            ICAPN2(ILAST:ILAST)='.'
            WRITE(ICAPN2(ILAST+1:ILAST+1),'(I1)')ICAPCN
          ELSE
            ICAPN2(IPEROD+2:ILAST+2)=ICAPN2(IPEROD:ILAST)
            ILAST=IPEROD
            ICAPN2(ILAST:ILAST)='_'
            WRITE(ICAPN2(ILAST+1:ILAST+1),'(I1)')ICAPCN
          ENDIF
        ENDIF
!
!       STEP 3: OPEN ICAPN2 FILE
!
        OPEN(ICPNU2,FILE=ICAPN2,STATUS="UNKNOWN",ACTION="WRITE")
      ENDIF
!
!     2019/08: RESTORE THE FOLLOWING 2 LINES
!
      IF(IFOUND.EQ.'YES')GO TO 8000
      IF(IERROR.EQ.'YES')GO TO 8100
!
!               ********************************************************
!               **  STEP 62--                                         **
!               **  NO MATCH FOUND FOR COMMAND; THEREFORE, CHECK TO   **
!               **  SEE IF A DEFAULT COMMAND EXISTS, IF SO, INSERT    **
!               **  THE DEFAULT COMMAND AND SEARCH AGAIN; IF NOT,     **
!               **  GENERATE A MESSAGE STATING THAT THERE WAS NO MATCH.*
!               ********************************************************
!
!     2018/09: IF COMMAND NOT FOUND AND COMMAND DOES NOT START WITH
!              "LET", CHECK FOR AN "=".  IF "=" FOUND, THEN INSERT
!              "LET" AT START OF LINE AND RETRY THE COMMAND.
!
!     2019/09: CHECK FIRST IF NUMBER OF ARGUMENTS IS ZERO.  IF SO,
!              THEN INSERT "PRINT" AT START OF LINE AND RETRY THE
!              COMMAND.
!
      ISTEPN='62'
      IF(IBUGMA.EQ.'ON'.OR.ISUBRO.EQ.'MAIN')   &
      CALL TRACE2(ISTEPN,ISUBN1,ISUBN2)
!
      IF((IDEFCM.EQ.'OFF' .OR. IDEFCM.EQ.'NO') .AND. IWIDDC.LE.0)THEN
        IF(NUMARG.EQ.0)THEN
          ISHIFT=1
          IERROR='NO'
          CALL SHIFTR(ISHIFT,IHARG,IHARG2,IARG,ARG,IARGT,NUMARG,   &
                      IBUGG2,IERROR)
          IF(IERROR.EQ.'YES')GO TO 8000
          DO 6340 II=IWIDTH,1,-1
            IANSLC(II+6)=IANSLC(II)
            IANS(II+6)=IANS(II)
 6340     CONTINUE
          IHARG(1)=ICOM
          IHARG2(1)=ICOM2
          ICOM='PRIN'
          ICOM2='T   '
          IANS(1)='P   '
          IANS(2)='R   '
          IANS(3)='I   '
          IANS(4)='N   '
          IANS(5)='T   '
          IANS(6)='    '
          IANSLC(1)='P   '
          IANSLC(2)='R   '
          IANSLC(3)='I   '
          IANSLC(4)='N   '
          IANSLC(5)='T   '
          IANSLC(6)='    '
          IWIDTH=IWIDTH+6
          CALL DPWRIT(IMACRO,IMACNU,IMACCS,   &
                      IFORSW,ICWRIF,NCWRIF,   &
                      IWRIRW,   &
                      IFORWI,IFORWR,MAXNWI,   &
                      IBUGS2,IBUGQ,ISUBRO,IFOUND,IERROR)
          IFOUND='YES'
          GO TO 8000
        ELSEIF(ICOM.NE.'LET ')THEN
          IFLAG=0
          DO 6230 II=1,NUMARG
            IF(IHARG(II).EQ.'=   ')THEN
              IFLAG=1
              GO TO 6239
            ENDIF
 6230     CONTINUE
 6239     CONTINUE
          IF(IFLAG.EQ.1 .AND. IWIDTH.LE.MAXSTR-4)THEN
            ISHIFT=1
            IERROR='NO'
            CALL SHIFTR(ISHIFT,IHARG,IHARG2,IARG,ARG,IARGT,NUMARG,   &
                        IBUGG2,IERROR)
            IF(IERROR.EQ.'YES')GO TO 8000
            DO 6240 II=IWIDTH,1,-1
              IANSLC(II+4)=IANSLC(II)
              IANS(II+4)=IANS(II)
 6240       CONTINUE
            IHARG(1)=ICOM
            IHARG2(1)=ICOM2
            ICOM='LET '
            ICOM2='    '
            IANS(1)='L   '
            IANS(2)='E   '
            IANS(3)='T   '
            IANS(4)='    '
            IANSLC(1)='L   '
            IANSLC(2)='E   '
            IANSLC(3)='T   '
            IANSLC(4)='    '
            IWIDTH=IWIDTH+4
            CALL DPLET(IANGLU,ISEED,IBUGA2,IBUGA3,IBUGCO,IBUGEV,IBUGQ,   &
                       TEMP,TEMP2,XTEMP1,XTEMP2,MAXOBV,   &
                       IFTEXP,IFTORD,IFORSW,IOSMET,   &
                       ROOTAC,OPTACC,IOPTME,IOPTHE,   &
                       ISUBRO,IFOUND,IERROR)
            IFOUND='YES'
            GO TO 8000
          ELSE
            GO TO 6249
          ENDIF
        ELSE
          GO TO 6249
        ENDIF
      ELSE
        GO TO 6249
      ENDIF
!
 6249 CONTINUE
      IF(IDEFCM.EQ.'OFF' .OR. IDEFCM.EQ.'NO')GO TO 8000
      IF(IWIDDC.LE.0)GO TO 8000
      IF(IPASS.GE.2)GO TO 8000
      IPASS=2
!
      DO 6210 I=1,IWIDTH
        IREV=IWIDTH-I+1
        JREV=IREV+IWIDDC+1
        IANSLC(JREV)=IANSLC(IREV)
        IANS(JREV)=IANS(IREV)
 6210 CONTINUE
!
      DO 6220 I=1,IWIDDC
        IANSLC(I)=IDEFC(I)
        IANS(I)=IDEFC(I)
 6220 CONTINUE
!
      I=IWIDDC+1
!CCCC THE FOLLOWING LINE WAS FIXED JULY 1989
!CCCC IANSLC(I)=' '
      IANSLC(I)='    '
      IANS(I)=' '
!
      GO TO 1490
!
!               ***********************************************
!               **  STEP 80--                                **
!               **  DETERMINE IF COMMAND FOUND OR NOT.       **
!               **  IF FOUND, LOOP BACK TO READ ANOTHER      **
!               **  LINE FROM THE TERMINAL.                  **
!               **  IF NOT FOUND, GENERATE AN ERROR MESSAGE  **
!               **  AND THEN LOOP BACK TO READ ANOTHER       **
!               **  LINE FROM THE TERMINAL.                  **
!               ***********************************************
!
 8000 CONTINUE
      ISTEPN='80'
      IF(IBUGMA.EQ.'ON'.OR.ISUBRO.EQ.'MAIN')   &
      CALL TRACE2(ISTEPN,ISUBN1,ISUBN2)
!
!     THE FOLLOWING 3 LINES WERE COMMENTED OUT DEC. 1988
!     AND REPLACED WITH THE SUCCEEDING 8 LINES
!
!CCCC IF(IFOUND.EQ.'YES'.AND.ICOM.NE.'RESE')GO TO 1000
!CCCC IF(IFOUND.EQ.'YES'.AND.ICOM.EQ.'RESE')GO TO 200
!CCCC GO TO 8100
!
      IF(IFOUND.EQ.'YES')THEN
        IF(ICOM.NE.'RESE')THEN
          GO TO 1000
        ELSEIF(ICOM.EQ.'RESE'.AND.NUMARG.LE.0)THEN
          IRSCNT=1
          GO TO 200
        ELSEIF(ICOM.EQ.'RESE'.AND.NUMARG.GE.1.AND.   &
          IHARG(1).EQ.'ALL')THEN
          IRSCNT=1
          GO TO 200
        ELSE
          GO TO 1000
        ENDIF
      ENDIF
!
 8100 CONTINUE
!
      WRITE(ICOUT,999)
      CALL DPWRST('XXX','BUG ')
      IF(IFOUND.EQ.'NO'.AND.IERROR.EQ.'NO')THEN
        WRITE(ICOUT,8111)
 8111   FORMAT('***** NO MATCH FOUND FOR COMMAND.')
        CALL DPWRST('XXX','BUG ')
      ELSEIF(IERROR.EQ.'YES')THEN
        WRITE(ICOUT,8112)
 8112   FORMAT('***** ERROR CONDITION ENCOUNTERED AT MAIN 8112.')
        CALL DPWRST('XXX','BUG ')
      ENDIF
!CCCC WRITE(ICOUT,8113)
!8113 FORMAT('      THE FIRST 8 CHARACTERS OF THE FIRST WORD ')
!CCCC CALL DPWRST('XXX','BUG ')
!CCCC WRITE(ICOUT,8114)ICOM,ICOM2
!8114 FORMAT('      OF THE COMMAND ARE ',2A4)
!CCCC CALL DPWRST('XXX','BUG ')
      WRITE(ICOUT,8113)
 8113 FORMAT('      THE ENTERED COMMAND LINE WAS AS FOLLOWS--')
      CALL DPWRST('XXX','BUG ')
      IF(IWIDTH.GE.1)THEN
        WRITE(ICOUT,8114)(IANS(I),I=1,MIN(100,IWIDTH))
 8114   FORMAT('      ',100A1)
        CALL DPWRST('XXX','BUG ')
      ENDIF
      WRITE(ICOUT,8115)
 8115 FORMAT('      PLEASE REENTER COMMAND LINE.')
      CALL DPWRST('XXX','BUG ')
      GO TO 1000
!
!               *****************
!               **  STEP 90--  **
!               **  EXIT.      **
!               *****************
!
!CCCC STOP
      END PROGRAM DATAPLOT
